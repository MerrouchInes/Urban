package modele;

public abstract class Objet {
	
	protected int posX ;
	protected int posY ;
	protected Label label ;
	
	/**
	 * getPosX
	 */
	public int getPosX() {
		return posX;
	}
	
	/**
	 * getPosY
	 */
	public int getPosY() {
		return posY;
	}
	
	/**
	 * getLabel
	 */
	public Label getLabel() {
		return label;
	}
	

}
